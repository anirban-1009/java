//Math class

public class ex44{
	public static void main(String[] args){
		int a = Math.abs(10);//absolute value
		int b = Math.abs(-20);

		double c = Math.ceil(7.342);//rounding floating value up

		double d = Math.floor(7.342);//rounding floating value down

		int m = Math.max(10,20);//maximum of two values

		int n = Math.min(10,20);//minimum of two values

		double p = Math.pow(2,3);//returns the power of the second parameter to the first

		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(m);
		System.out.println(n);
		System.out.println(p);
	}
}